import random

# لیست آیتم‌ها و درصدها (با پوچ و آبمیوه)
items = [
    ("سافتلن آبی", 4),
    ("سافتلن سبز", 4),
    ("عسل کوچک", 3),
    ("عسل بزرگ", 1),
    ("ژل ماشین ظرفشویی", 6),
    ("بیسکویت کاکائویی", 7),
    ("چای رفاه لاهیجان", 7),
    ("نودل شف‌هو", 12),
    ("سس گوجه‌فرنگی بیژن", 9),
    ("عرق نعناع", 9),
    ("مایع لباسشویی اکتیو", 7),
    ("نوشابه", 10),
    ("نقل خرسند", 8),
    ("قرص جرم‌گیر ماشین لباسشویی ۱۴ عددی", 7),
    ("مایع براق‌کننده ماشین ظرفشویی", 8),
    ("قرص جرم‌گیر ماشین ظرفشویی ۵ عددی", 8),
    ("قرص ماشین ظرفشویی ۲۸ عددی", 6),
    ("نمک مخصوص ماشین ظرفشویی", 7),
    ("پودر ماشین ظرفشویی", 6),
    ("نودلیت سبزیجات", 12),
    ("نودلیت مرغ", 12),
    ("نودلیت گوشت", 12),
    ("ظرف در بسته", 8),
    ("کنسرو لوبیا", 8),
    ("پوچ", 5),
    ("آبمیوه – ۵۰ هزار تومان", 5)  # آیتم جدید اضافه شد
]

# آماده کردن لیست برای انتخاب بر اساس درصد
choices = []
for item, weight in items:
    choices.extend([item] * weight)

# تابع چرخش گردونه
def spin():
    result = random.choice(choices)
    return f'🔹 نتیجه: {result}'

# اجرای برنامه
if __name__ == "__main__":
    input("برای چرخش گردونه Enter را بزنید...")
    print(spin())
    input("برای خروج Enter را بزنید...")