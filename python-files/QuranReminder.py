def show_random_sura():
    sura = random.choice(suwar)
    root = tk.Tk()
    root.withdraw()
    messagebox.showinfo("اقتراح سورة", f"اقرأ سورة: {sura}")
    root.destroy()

def start_timer():
    while True:
        time.sleep(60 * 60)
        show_random_sura()

