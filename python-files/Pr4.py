# Создаем массив из 10 целых чисел  
array = [5, -3, 8, -1, 0, -7, 2, -4, 6, -9]  
  
# Инициализируем счетчик отрицательных чисел  
negative_count = 0  
  
# Проходим по всем элементам массива  
for num in array:  
    # Проверяем, является ли число отрицательным  
    if num < 0:  
        negative_count += 1  
  
# Выводим результат  
print(f"Количество отрицательных чисел в массиве: {negative_count}")  

