def clear_screen():
    """–û—á–∏—Å—Ç–∏—Ç—å —ç–∫—Ä–∞–Ω"""
    os.system('cls' if os.name == 'nt' else 'clear')

def type_text(text, delay=0.03):
    """–ü–µ—á–∞—Ç—å —Ç–µ–∫—Å—Ç–∞ —Å —ç—Ñ—Ñ–µ–∫—Ç–æ–º –ø–µ—á–∞—Ç–Ω–æ–π –º–∞—à–∏–Ω–∫–∏"""
    for char in text:
        print(char, end='', flush=True)
        time.sleep(delay)
    print()

def loading_bar(duration=3, description=""):
    """–†–µ–∞–ª–∏—Å—Ç–∏—á–Ω–∞—è –∞–Ω–∏–º–∏—Ä–æ–≤–∞–Ω–Ω–∞—è –ø–æ–ª–æ—Å–∞ –∑–∞–≥—Ä—É–∑–∫–∏ —Å —ç—Ñ—Ñ–µ–∫—Ç–∞–º–∏"""
    print(f"{description}")
    
    # –§–∞–∑–∞ 1: –ú–µ–¥–ª–µ–Ω–Ω—ã–π —Å—Ç–∞—Ä—Ç
    for i in range(15):
        progress = int((i / 15) * 30)
        bar = "‚ñà" * (progress // 3) + "‚ñì" * min(2, 10 - progress // 3) + "‚ñë" * max(0, 8 - progress // 3)
        percentage = int((i / 15) * 30)
        
        # –î–æ–±–∞–≤–ª—è–µ–º –º–µ—Ä—Ü–∞—é—â–∏–π —ç—Ñ—Ñ–µ–∫—Ç
        if random.random() < 0.3:
            bar = bar.replace("‚ñì", "‚ñí")
        
        print(f"\r[{bar}] {percentage}% {'.' * (i % 4)}", end='', flush=True)
        time.sleep(duration / 40)
    
    # –§–∞–∑–∞ 2: –£—Å–∫–æ—Ä–µ–Ω–∏–µ
    for i in range(15, 35):
        progress = 30 + int(((i - 15) / 20) * 50)
        bar_filled = progress // 5
        bar_loading = min(2, 20 - bar_filled) if bar_filled < 20 else 0
        bar_empty = max(0, 20 - bar_filled - bar_loading)
        
        bar = "‚ñà" * bar_filled + "‚ñì" * bar_loading + "‚ñë" * bar_empty
        
        # –°–ª—É—á–∞–π–Ω—ã–µ –≥–ª–∏—Ç—á–∏
        if random.random() < 0.1:
            glitch_pos = random.randint(0, len(bar) - 1)
            bar = bar[:glitch_pos] + random.choice("‚ñí‚ñì‚ñà") + bar[glitch_pos + 1:]
        
        print(f"\r[{bar}] {progress}% {'‚ñ∂' if i % 2 else '‚ñ∑'}", end='', flush=True)
        time.sleep(duration / 50)
    
    # –§–∞–∑–∞ 3: –ó–∞–º–µ–¥–ª–µ–Ω–∏–µ –∏ –∑–∞–≤–µ—Ä—à–µ–Ω–∏–µ
    for i in range(35, 45):
        progress = 80 + int(((i - 35) / 10) * 20)
        bar = "‚ñà" * (progress // 5) + "‚ñë" * (20 - progress // 5)
        
        # –≠—Ñ—Ñ–µ–∫—Ç "–ø–æ–¥–≤–∏—Å–∞–Ω–∏—è" –≤ –∫–æ–Ω—Ü–µ
        if i > 40:
            time.sleep(duration / 25)
        
        spinner = "|/-\\"[i % 4]
        print(f"\r[{bar}] {progress}% {spinner}", end='', flush=True)
        time.sleep(duration / 60)
    
    # –§–∏–Ω–∞–ª—å–Ω—ã–π —ç—Ñ—Ñ–µ–∫—Ç
    final_bar = "‚ñà" * 20
    print(f"\r[{final_bar}] 100% ‚úì –ó–ê–í–ï–†–®–ï–ù–û", flush=True)
    time.sleep(0.5)

