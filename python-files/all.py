# Программа для сложения двух чисел

# Запрос ввода первого числа
num1 = input("Введите первое число: ")

# Запрос ввода второго числа
num2 = input("Введите второе число: ")

# Преобразование введённых значений из строк в числа
try:
    num1 = float(num1)
    num2 = float(num2)
except ValueError:
    print("Ошибка: Введены некорректные числа.")
    exit()

# Сложение чисел
result = num1 + num2

# Вывод результата
print(f"Результат сложения: {result}")