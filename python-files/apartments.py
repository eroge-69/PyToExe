# Простая программа для поиска цены квартиры по номеру

# База данных квартир (номер: цена)
apartments = {
    101: 25000,
    102: 27000,
    103: 30000,
    104: 28000,
    105: 35000
}

print("=== Поиск квартиры по номеру ===")
try:
    number = int(input("Введите номер квартиры: "))
    if number in apartments:
        print(f"Квартира {number}, цена: {apartments[number]} руб.")
    else:
        print("Такой квартиры нет в базе.")
except ValueError:
    print("Ошибка: введите номер числом.")
