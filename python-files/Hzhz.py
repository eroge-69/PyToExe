    t.pensize(10 + 5*i)
    t.pencolor("red")
    t.lt(45)
    t.forward(50)
    t.rt(45)
    t.fd(80)
    t.rt(45)
    t.fd(50)
    t.rt(45)
    t.fd(60)
    t.rt(40)
    t.fd(235)
    t.rt(100)
