import matplotlib.pyplot as plt  # Модуль matplotlib для построения графиков
import os                        # Модуль os для работы с путями и файловой системой

# === Определение пути к файлу с данными ===
# os.path.abspath(__file__) — абсолютный путь к текущему исполняемому скрипту
# os.path.dirname(...) — извлекает из него только путь к директории
script_dir = os.path.dirname(os.path.abspath(__file__))

# Формируем полный путь к файлу данных "V_E_pr.txt", который лежит в той же папке, что и программа
data_file = os.path.join(script_dir, 'V_E_pr.txt')

# === Чтение данных из файла ===
# Открываем файл в режиме чтения ('r')
# with автоматически закроет файл после выхода из блока
with open(data_file, 'r') as file:
    # Читаем файл построчно, удаляем лишние пробелы/переносы (\n) с помощью strip()
    # Преобразуем каждую строку в число с плавающей точкой (float)
    data = [float(line.strip()) for line in file]

# === Создание временной оси ===
# Здесь предполагается, что каждая строка в файле — это одно измерение функции за равный интервал времени
# list(range(len(data))) создаёт список индексов [0, 1, 2, ... N-1]
time = list(range(len(data)))

# === Построение графика ===
plt.plot(time, data)             # Рисуем линию: ось X — время, ось Y — считанные значения функции
plt.xlabel('Time')               # Подпись оси X
plt.ylabel('Function Value')     # Подпись оси Y
plt.title('Function Plot')       # Заголовок графика
plt.grid(True)                   # Включаем сетку для удобства чтения графика
plt.show()                       # Отображаем график на экране
