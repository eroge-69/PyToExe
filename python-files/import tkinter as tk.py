def get_days_together_details():
    start_date = datetime(2018, 12, 12)
    today = datetime.today()
    delta = today - start_date
    days = delta.days
    weeks = days // 7
    remaining_days = days % 7
    years = days // 365
    months = (days % 365) // 30
    extra_days = (days % 365) % 30
    return days, weeks, remaining_days, years, months, extra_days

