def calculate_time():
    print("\nüßÆ –ö–∞–ª—å–∫—É–ª—è—Ç–æ—Ä –≤—Ä–µ–º–µ–Ω–∏ –¥–ª—è –ø–∏–∫—Å–µ–ª—å-–∞—Ä—Ç–∞")
