        people_count = int(input("–ö–æ–ª–∏—á–µ—Å—Ç–≤–æ –ª—é–¥–µ–π: "))
        hours_per_day = float(input("–°–∫–æ–ª—å–∫–æ —á–∞—Å–æ–≤ –≤ –¥–µ–Ω—å –Ω–∞ –∞—Ä—Ç?: "))
        
